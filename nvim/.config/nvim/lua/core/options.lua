local o = vim.opt

-- General options
o.swapfile = false        -- swap file deactivation
o.undofile = true         -- no limit in undo history

-- File numbers
o.relativenumber = true   -- use relative number lines
o.number = true           -- except for current line

-- Tabs and Indentation
o.tabstop = 2			        -- Two spaces for tabs
o.shiftwidth = 2		      -- Two spaces for identations size
o.expandtab = true	      -- Changes tabs in spaces


